void clearConsole()
{
	cursorX = 0;
	cursorY = 0;
	
	int i;
	for (i=0; i<80*25; i++)
	{
		grid[i].c = 0;
		grid[i].attr = 0x07;
	};
};

void scroll()
{
	int i;
	for (i=0; i<80*24; i++)
	{
		grid[i].c = grid[i+80].c;
		grid[i].attr = grid[i+80].attr;
	};
	
	for (i=80*24; i<80*25; i++)
	{
		grid[i].c = 0;
		grid[i].attr = currentAttr;
	};
	
	cursorY--;
};

void writeConsole(const char *buf, size_t sz)
{
	while (sz--)
	{
		char c = *buf++;

		if (escapePos != -1)
		{
			if (escapePos == 4)
			{
				// discard
				escapePos = -1;
			}
			else
			{
				escapeBuffer[escapePos++] = c;
				
				if ((escapeBuffer[0] == 33) && (escapePos == 1))
				{
					clearConsole();
					escapePos = -1;
				}
				else if ((escapeBuffer[0] == 34) && (escapePos == 2))
				{
					currentAttr = (uint8_t) escapeBuffer[1];
					escapePos = -1;
				}
				else if ((escapeBuffer[0] == 35) && (escapePos == 3))
				{
					cursorX = (int) escapeBuffer[1] % 80;
					cursorY = (int) escapeBuffer[2] % 25;
					escapePos = -1;
				};
			};
		}
		else if (c == '\e')
		{
			escapePos = 0;
		}
		else if (c == '\b')
		{
			if ((cursorX == 0) && (cursorY == 0))
			{
				continue;
			};
			
			if (cursorX == 0)
			{
				cursorX = 79;
				cursorY--;
			}
			else
			{
				cursorX--;
			};
			
			grid[cursorY * 80 + cursorX].c = 0;
		}
		else if (c == '\n')
		{
			cursorY++;
			cursorX = 0;
		}
		else if (c == '\t')
		{
			cursorX = (cursorX & ~7) + 8;
			if (cursorX > 80)
			{
				cursorX = 0;
				cursorY++;
			};
		}
		else if (c == '\r')
		{
			cursorX = 0;
		}
		else
		{
			grid[cursorY * 80 + cursorX].c = c;
			grid[cursorY * 80 + cursorX].attr = currentAttr;
			cursorX++;
			if (cursorX == 80)
			{
				cursorX = 0;
				cursorY++;
			};
		};
	
		if (cursorY == 25) scroll();
	};
};

void renderConsole(DDISurface *surface)
{
	int x, y;
	for (x=0; x<80; x++)
	{
		for (y=0; y<25; y++)
		{	
			ConsoleCell *cell = &grid[y * 80 + x];
			
			uint8_t attr = cell->attr;
			int pos = y * 80 + x;
			if ((pos >= selectStart) && (pos < selectEnd)) attr = 0x20;
			
			DDIColor *background = &consoleColors[attr >> 4];
			DDIColor *foreground = &consoleColors[attr & 0xF];
			
			unsigned int index = (unsigned char) cell->c;
			const char *bmp = &font[index * 16];
			ddiFillRect(surface, x*9, y*16, 9, 16, background);
			ddiExpandBitmap(surface, x*9, y*16, DDI_BITMAP_8x16, bmp, foreground);
		};
	};
	
	// cursor
	ddiFillRect(surface, cursorX*9, cursorY*16+14, 9, 2, &consoleColors[7]);
	gwmPostDirty(termWindow);
};

void *ctrlThreadFunc(void *context)
{
	char **argv = (char**) context;

	// this thread accepts all signals
	sigset_t sigset;
	sigfillset(&sigset);
	pthread_sigmask(SIG_UNBLOCK, &sigset, NULL);
	
	pid_t pid = fork();
	if (pid == -1)
	{
		perror("fork");
		close(fdMaster);
		gwmQuit();
		exit(1);
	}
	else if (pid == 0)
	{
		setsid();
		int fd = open(ptsname(fdMaster), O_RDWR);
		if (fd == -1)
		{
			exit(1);
		};
		
		close(0);
		close(1);
		close(2);
		
		dup2(fd, 0);
		dup2(fd, 1);
		dup2(fd, 2);
		
		if (argv[1] == NULL)
		{
			execl("/bin/sh", "sh", NULL);
		}
		else
		{
			argv[0] = "/usr/bin/env";
			execv("/usr/bin/env", argv);
		};
		
		perror("exec");
		exit(1);
	};
	
	while (1)
	{
		char buffer[4096];
		ssize_t sz = read(fdMaster, buffer, 4096);
		if (sz > 0L)
		{
			pthread_mutex_lock(&consoleLock);
			writeConsole(buffer, sz);
			pthread_mutex_unlock(&consoleLock);
			gwmPostUpdate(termWindow);
		};
		
		if (!running)
		{
			gwmPostUpdate(termWindow);
			waitpid(pid, NULL, 0);
			break;
		};
	};
	
	return NULL;
};

static int getPositionFromCoords(int cx, int cy)
{
	int tx = cx / 9;
	int ty = cy / 16;
	return ty * 80 + tx;
};

int terminalHandler(GWMEvent *ev, GWMWindow *ignore, void *context)
{
	DDISurface *surface = gwmGetWindowCanvas(termWindow);
	
	if (ev->type == GWM_EVENT_CLOSE)
	{
		return GWM_EVSTATUS_BREAK;
	}
	else if (ev->type == GWM_EVENT_UPDATE)
	{
		pthread_mutex_lock(&consoleLock);
		renderConsole(surface);
		pthread_mutex_unlock(&consoleLock);
		
		if (!running) return GWM_EVSTATUS_BREAK;
	}
	else if (ev->type == GWM_EVENT_DOWN)
	{
		if (ev->keymod & GWM_KM_CTRL)
		{
			char put = 0;
			if (ev->keycode == 'c')
			{
				put = 0x83;	// CC_VINTR
			}
			else if (ev->keycode == 'k')
			{
				put = 0x84;	// CC_VKILL
			};
			
			if (put != 0)
			{
				write(fdMaster, &put, 1);
			};
		}
		else if (ev->keycode == GWM_KC_MOUSE_LEFT)
		{
			selectStart = selectEnd = selectAnchor = getPositionFromCoords(ev->x, ev->y);
			pthread_mutex_lock(&consoleLock);
			renderConsole(surface);
			pthread_mutex_unlock(&consoleLock);
		}
		else if (ev->keycode == GWM_KC_LEFT)
		{
			write(fdMaster, "\x8D", 1);
		}
		else if (ev->keycode == GWM_KC_RIGHT)
		{
			write(fdMaster, "\x8E", 1);
		}
		else if (ev->keycode == GWM_KC_UP)
		{
			write(fdMaster, "\x8B", 1);
		}
		else if (ev->keycode == GWM_KC_DOWN)
		{
			write(fdMaster, "\x8C", 1);
		}
		else
		{
			char c = (char) ev->keychar;
			if (c != 0)
			{
				write(fdMaster, &c, 1);
			};
		};
	}
	else if (ev->type == GWM_EVENT_MOTION)
	{
		if (selectAnchor != -1)
		{	
			int nowAt = getPositionFromCoords(ev->x, ev->y);
			
			if (nowAt < selectAnchor)
			{
				selectStart = nowAt;
				selectEnd = selectAnchor;
			}
			else
			{
				selectStart = selectAnchor;
				selectEnd = nowAt;
			};

			pthread_mutex_lock(&consoleLock);
			renderConsole(surface);
			pthread_mutex_unlock(&consoleLock);
		};
	}
	else if (ev->type == GWM_EVENT_UP)
	{
		if (ev->keycode == GWM_KC_MOUSE_LEFT)
		{
			selectAnchor = -1;
		};
	};
	
	return GWM_EVSTATUS_OK;
};

int main(int argc, char *argv[])
{
	if (gwmInit() != 0)
	{
		fprintf(stderr, "Failed to initialize GWM!\n");
		return 1;
	};
	
	GWMWindow *wnd = gwmCreateWindow(NULL, "Terminal", 10, 10, 720, 400, GWM_WINDOW_MKFOCUSED);
	termWindow = wnd;
	
	gwmSetWindowCursor(wnd, GWM_CURSOR_TEXT);
	
	DDISurface *surface = gwmGetWindowCanvas(wnd);
	
	const char *error;
	DDISurface *icon = ddiLoadAndConvertPNG(&surface->format, "/usr/share/images/terminal.png", &error);
	if (icon == NULL)
	{
		printf("Failed to load terminal icon: %s\n", error);
	}
	else
	{
		gwmSetWindowIcon(wnd, icon);
	};
	
	clearConsole();
	surface = gwmGetWindowCanvas(wnd);
	renderConsole(surface);
	
	fdMaster = getpt();
	if (fdMaster == -1)
	{
		perror("getpt");
		gwmQuit();
		return 1;
	};
	
	grantpt(fdMaster);
	unlockpt(fdMaster);
	
	// this thread will not accept any signals
	sigset_t sigset;
	sigfillset(&sigset);
	pthread_sigmask(SIG_BLOCK, &sigset, NULL);
	
	pthread_create(&ctrlThread, NULL, ctrlThreadFunc, argv);
	
	gwmPushEventHandler(wnd, terminalHandler, NULL);
	gwmMainLoop();
	gwmQuit();
	
	return 0;
};
